function arrayBuilder(one, two, three) {
    var arr = [];
    arr.push(one);
    arr.push(two);
    arr.push(three);
    //return the newly built array:
    return arr;
}
//save this function call to a variable:
var simpleArr = arrayBuilder("apple", "plum", "pear");

//console.log the values stored in the variable:
console.log(simpleArr);


